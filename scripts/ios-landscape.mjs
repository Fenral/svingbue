#!/usr/bin/env node
// scripts/ios-landscape.mjs
//
// Patches ios/App/App/Info.plist AFTER `npx cap add ios` has generated the
// Xcode project (this only exists on the Codemagic Mac runner — there is no
// ios/ folder on Windows dev machines, so this script is a no-op/error there
// until CI creates it). Run this in the Codemagic pipeline, after
// `npx cap add ios` and before `npx cap sync ios`.
//
// What it does:
//   1. Sets UISupportedInterfaceOrientations (iPhone) to landscape-left +
//      landscape-right ONLY (removes portrait/upside-down).
//   2. Sets UISupportedInterfaceOrientations~ipad (iPad) the same way, so
//      the app stays landscape-only even if later run on iPad.
//   3. Sets UIRequiresFullScreen to true (no Slide Over / Split View on
//      iPad, which would otherwise fight the landscape-only layout).
//   4. Sets CFBundleDisplayName to "StrikeArc" (the name shown under the
//      home-screen icon; separate from CFBundleName).
//
// Implementation notes / assumptions:
//   - This is a plain-text/regex plist editor, not a full plist parser —
//     Info.plist as generated by `cap add ios` is a standard XML plist, and
//     we only ever touch a handful of well-known top-level keys, so regex
//     string surgery is simpler and has zero extra dependencies (no need
//     for `plutil`, which isn't available cross-platform anyway).
//   - Idempotent: safe to run multiple times. If a key already exists with
//     the values we want, re-running produces no diff. If a key exists with
//     different values, we replace its value. If a key is entirely absent,
//     we insert it (right before the closing `</dict>` of the root dict).
//   - Assumes the plist's root <dict> is the last `</dict>` before
//     `</plist>` in the file — true for every plist Capacitor/Xcode
//     generates (a single top-level dict).

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const PLIST_PATH = join(ROOT, 'ios', 'App', 'App', 'Info.plist');

function log(msg) {
  console.log(`[ios-landscape] ${msg}`);
}

if (!existsSync(PLIST_PATH)) {
  console.error(
    `[ios-landscape] ERROR: ${PLIST_PATH} does not exist.\n` +
    `This script must run AFTER "npx cap add ios" has generated the iOS\n` +
    `project (Codemagic mac_mini_m2 build step), not on a Windows dev\n` +
    `machine where ios/ is never generated locally.`
  );
  process.exit(1);
}

let plist = readFileSync(PLIST_PATH, 'utf8');
const original = plist;

const LANDSCAPE_ONLY_ARRAY = [
  'UIInterfaceOrientationLandscapeLeft',
  'UIInterfaceOrientationLandscapeRight',
];

function buildStringArrayXml(values, indent = '\t') {
  const items = values.map((v) => `${indent}\t<string>${v}</string>`).join('\n');
  return `<array>\n${items}\n${indent}</array>`;
}

/**
 * Replace the value of an existing <key>keyName</key><array>...</array> (or
 * any single-node value) block, or insert a new <key>/value pair just before
 * the root dict's closing </dict> if the key doesn't exist yet.
 */
function setArrayKey(xml, keyName, values) {
  const keyRe = new RegExp(
    `(<key>${keyName}</key>\\s*)<array>[\\s\\S]*?</array>`,
    'm'
  );
  const newArrayXml = buildStringArrayXml(values);
  if (keyRe.test(xml)) {
    return xml.replace(keyRe, `$1${newArrayXml}`);
  }
  return insertKeyBeforeRootDictClose(
    xml,
    `\t<key>${keyName}</key>\n\t${newArrayXml}`
  );
}

function setBoolKey(xml, keyName, boolValue) {
  const tag = boolValue ? 'true' : 'false';
  const keyRe = new RegExp(
    `(<key>${keyName}</key>\\s*)<(?:true|false)\\s*/>`,
    'm'
  );
  if (keyRe.test(xml)) {
    return xml.replace(keyRe, `$1<${tag}/>`);
  }
  return insertKeyBeforeRootDictClose(xml, `\t<key>${keyName}</key>\n\t<${tag}/>`);
}

function setStringKey(xml, keyName, stringValue) {
  const keyRe = new RegExp(
    `(<key>${keyName}</key>\\s*)<string>[^<]*</string>`,
    'm'
  );
  if (keyRe.test(xml)) {
    return xml.replace(keyRe, `$1<string>${stringValue}</string>`);
  }
  return insertKeyBeforeRootDictClose(
    xml,
    `\t<key>${keyName}</key>\n\t<string>${stringValue}</string>`
  );
}

/**
 * Inserts a raw XML snippet immediately before the LAST `</dict>` that
 * precedes `</plist>` — i.e. the root dict's closing tag. Capacitor/Xcode
 * plists always have exactly one top-level <dict>...</dict>, so the last
 * </dict> before </plist> is unambiguous.
 */
function insertKeyBeforeRootDictClose(xml, snippet) {
  const plistCloseIdx = xml.lastIndexOf('</plist>');
  if (plistCloseIdx === -1) {
    throw new Error('[ios-landscape] Info.plist has no </plist> — not a valid plist file.');
  }
  const dictCloseIdx = xml.lastIndexOf('</dict>', plistCloseIdx);
  if (dictCloseIdx === -1) {
    throw new Error('[ios-landscape] Info.plist has no </dict> before </plist> — malformed plist.');
  }
  return xml.slice(0, dictCloseIdx) + snippet + '\n' + xml.slice(dictCloseIdx);
}

// 1. iPhone orientations — landscape only.
plist = setArrayKey(plist, 'UISupportedInterfaceOrientations', LANDSCAPE_ONLY_ARRAY);
log('set UISupportedInterfaceOrientations -> landscape-left + landscape-right only');

// 2. iPad orientations — landscape only (separate key Capacitor generates by default).
plist = setArrayKey(plist, 'UISupportedInterfaceOrientations~ipad', LANDSCAPE_ONLY_ARRAY);
log('set UISupportedInterfaceOrientations~ipad -> landscape-left + landscape-right only');

// 3. Full screen on iPad — prevents Slide Over/Split View fighting the layout.
plist = setBoolKey(plist, 'UIRequiresFullScreen', true);
log('set UIRequiresFullScreen -> true');

// 4. Display name shown under the home-screen icon.
plist = setStringKey(plist, 'CFBundleDisplayName', 'StrikeArc');
log('set CFBundleDisplayName -> StrikeArc');

if (plist === original) {
  log('no changes needed (already patched) — plist untouched on disk.');
} else {
  writeFileSync(PLIST_PATH, plist, 'utf8');
  log(`wrote ${PLIST_PATH}`);
}

log('done.');
